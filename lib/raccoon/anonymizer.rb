module Raccoon
  module Anonymizer
    # The intendeed use of this module is to anonymize the user that uses this tool.
    #
    # Anonymizer works like this:
    #
    # racoon anonymizer start
    #
    # Then a small input output flow starts.
    #
    # User wants to change mac address? If yes then run ./mac_addres_changer.sh in the root dir of this project.
    #
    # User wants to use a vpn or setup a proxy ?
    #
    # Wants to use proxychains?
    #
    # Wants to anonymize you future commands?
    #
    # If the user say yes, then it creates an environment where all next commands always will pass throug proxychains or vpn.
    #
  end
end
